<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class SendUnPaidInvoiceMail extends Mailable
{
    use Queueable, SerializesModels;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    private $data;
    public function __construct($data)
    {
        $this->data=$data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        $data=$this->data;
        return $this->subject($data['invoice_detail']['property'] ." (". $data['appartment'] .") ".  "Unpaid Invoice")->from(config('mail.from_address'))->cc(config('mail.admin_address'),"Admin")->view('emails.unpaid_invoice',compact('data'));
    }
}
